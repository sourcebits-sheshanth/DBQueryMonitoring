package com.entity;

public class Population {

	private int id;
	private int year;
	private int humans;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getHumans() {
		return humans;
	}

	public void setHumans(int humans) {
		this.humans = humans;
	}

}
